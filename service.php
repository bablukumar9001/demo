            var index = i;
            var panel = panels[i];
            var numToSubtract = panels.length - index;
            var remainingScroll = numToSubtract * panelWidth;
            var panelScrollPos = totalHeight - remainingScroll;
            $(panel).attr("data-scroll", panelScrollPos);
